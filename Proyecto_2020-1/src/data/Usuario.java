
package data;


public class Usuario {
    private String nombre;
    private int id;
    private String usuario;
    private String contraseña;
    private int cuenta;

    public String getNombre() {
        return nombre;
    }

    public int getId() {
        return id;
    }

    public String getUsuario() {
        return usuario;
    }

    public String getContraseña() {
        return contraseña;
    }

    public int getCuenta() {
        return cuenta;
    }
    
    
}
